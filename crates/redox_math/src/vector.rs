//! 2D, 3D, and 4D vector types.

/// A 2‑dimensional vector.
///
/// Re‑exported from [`glam::Vec2`]. Commonly used for texture coordinates,
/// 2D transforms, and screen positions.
pub type Vec2 = glam::Vec2;

/// A 3‑dimensional vector.
///
/// Re‑exported from [`glam::Vec3`]. Used for positions, directions, colors,
/// and scales in 3D space.
pub type Vec3 = glam::Vec3;

/// A 4‑dimensional vector.
///
/// Re‑exported from [`glam::Vec4`]. Typically used for homogeneous coordinates,
/// RGBA colors, or plane equations.
pub type Vec4 = glam::Vec4;